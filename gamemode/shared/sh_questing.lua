QUEST_TYPES = {
	[1] = {
		Name = "FindItem", ID = 1,
		Description = GetTString("Please find it!"),
		Lines = function(strs, meta) 
			strs[2] = Color(39, 174, 96)
			strs[3] = QUEST_ITEMS[meta.ItemID].Name
			strs[4] = Color(255,255,255)
			return strs
		end,
		GetMeta = function()
			return {
				ItemID = math.random(#QUEST_ITEMS),
				Points = math.random(5,10),
				Other = math.random(1, #CLIQUES)
			}
		end,
		QuestAccepted = function(ply, meta)
			return SpawnQuestItem(ply, meta.ItemID)
		end,
		QuestFailedCleanup = function(meta)
			meta.qents:Remove()
		end,
		QuestAbort = function(ply, meta)
			local e = Entity(meta.entID)

			if e then
				local c = e:GetClique()
				local curXP = ply:dbGetValue("cliques" .. c) or 10
				local endXP = curXP - math.floor(meta.Points / 2)

				if endXP < 8 then
					endXP = 8
				end

				ply:dbSetValue("cliques" .. c, endXP)
			end
		end,
		QuestCompleted = function(ply, meta)
			local e = Entity(meta.entID)

			if e then
				local c = e:GetClique()
				local curXP = ply:dbGetValue("cliques" .. c) or 10
				ply:dbSetValue("cliques" .. c, curXP + meta.Points)

				if c > 1 then
					local other = meta.Other
					if other == c then
						other = (other % #CLIQUES) + 1
					end
					local curXP = ply:dbGetValue("cliques" .. other) or 10

					local endXP = curXP - math.floor(meta.Points / 2)

					if endXP < 8 then
						endXP = 8
					end

					ply:dbSetValue("cliques" .. other, endXP)
				end
			end
		end,
		QuestReward = function(ply, meta)
			local e = Entity(meta.entID)

			if e then
				local c = e:GetClique()
				if c and CLIQUES[c] then
					local cname = CLIQUES[c].GroupName
					if c > 1 then
						local other = meta.Other
						if other == c then
							other = (other % #CLIQUES) + 1
						end
						local oname = CLIQUES[other].GroupName

						return {
							"+" .. meta.Points,
								ClientConfig.OverheadGradeColor(e, 1),
								cname,
								Color(255,255,255),
								GetTString("Respect") .. ".",
								"\n-" .. math.floor(meta.Points / 2),
								ClientConfig.GetCliqueColor(other, 1),
								oname,
								Color(255,255,255),
								GetTString("Respect") .. ".",
							}
					else
						return {
							"+" .. meta.Points,
								ClientConfig.OverheadGradeColor(e, 1),
								cname,
								Color(255,255,255),
								GetTString("Respect") .. "."
							}
					end
				else
					print("Nope.")
					print(CLIQUES[c])
					return {"None"}
				end
			else
				print ("Entity was null, lol.")
				return "None."
			end
		end,
	},
	[2] = {
		Name = "CliqueInitiation", ID = 2,
		Description = GetTString("Please find it!"),
		Lines = function(strs, meta) 
			strs[2] = Color(39, 174, 96)
			strs[3] = QUEST_ITEMS[meta.ItemID].Name
			strs[4] = Color(255,255,255)
